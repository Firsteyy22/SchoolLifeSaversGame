const hoverGroup6 = document.getElementById('handchem');
const hoverImage6 = document.getElementById('hover-image6');

// Show the image on hover
hoverGroup6.addEventListener('mouseover', () => {
    hoverImage6.style.display = 'block';
});

// Hide the image when the mouse leaves
hoverGroup6.addEventListener('mouseout', () => {
    hoverImage6.style.display = 'none';
});


