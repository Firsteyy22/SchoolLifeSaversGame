const hoverGroup14 = document.getElementById('bloodnose');
const hoverImage14 = document.getElementById('hover-image14');

// Show the image on hover
hoverGroup14.addEventListener('mouseover', () => {
    hoverImage14.style.display = 'block';
});

// Hide the image when the mouse leaves
hoverGroup14.addEventListener('mouseout', () => {
    hoverImage14.style.display = 'none';
});


