const hoverGroup10 = document.getElementById('bee');
const hoverImage10 = document.getElementById('hover-image10');

// Show the image on hover
hoverGroup10.addEventListener('mouseover', () => {
    hoverImage10.style.display = 'block';
});

// Hide the image when the mouse leaves
hoverGroup10.addEventListener('mouseout', () => {
    hoverImage10.style.display = 'none';
});


